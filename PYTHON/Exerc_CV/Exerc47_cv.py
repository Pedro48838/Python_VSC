'''Crie um programa que mostre na tela todos os números pares que estão no intervalo entre 1 e 50.'''

for c in range(1,51):
    if c % 2 ==0:
        print(c, end=' ') #o comando ,end= ' ' é para que os números fiquem em linha reta
print("Acabou!!")

for c in range(2,51, 2):
    print(c, end= ' ')
print("Acabou!!")  #faz com que use menos o processador




















